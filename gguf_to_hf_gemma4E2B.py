def _inverse_softplus(y: torch.Tensor) -> torch.Tensor:
    """log(exp(y) - 1).  Numerically stable for y > ~20 via log(e^y - 1) ≈ y."""
    return torch.where(y > 20.0, y, torch.log(torch.expm1(y.clamp_min(1e-9))))


def convert_audio_tensor(name: str, tensor: torch.Tensor):
    """Yield (hf_name, hf_tensor) for an audio-side mmproj GGUF tensor."""

    if name in ("a.conv1d.0.weight", "a.conv1d.1.weight"):
        bid = name.split(".")[2]
        yield (
            f"model.audio_tower.subsample_conv_projection.layer{bid}.conv.weight",
            tensor,
        )
        return
    if name in ("a.conv1d.0.norm.weight", "a.conv1d.1.norm.weight"):
        bid = name.split(".")[2]
        yield (
            f"model.audio_tower.subsample_conv_projection.layer{bid}.norm.weight",
            tensor,
        )
        return
    if name == "a.input_projection.weight":
        yield (
            "model.audio_tower.subsample_conv_projection.input_proj_linear.weight",
            tensor,
        )
        return

    if name == "a.pre_encode.out.weight":
        yield "model.audio_tower.output_proj.weight", tensor
        return
    if name == "a.pre_encode.out.bias":
        yield "model.audio_tower.output_proj.bias", tensor
        return

    if name == "mm.a.input_projection.weight":
        yield "model.embed_audio.embedding_projection.weight", tensor
        return

    if name.startswith("a.blk."):
        parts = name.split(".", 3)
        try:
            i = int(parts[2])
        except ValueError:
            return
        sub = parts[3]

        if sub == "per_dim_scale.weight":
            yield (
                f"model.audio_tower.layers.{i}.self_attn.per_dim_scale",
                _inverse_softplus(tensor.to(torch.float32)),
            )
            return

        if sub == "conv_dw.weight":
            yield (
                f"model.audio_tower.layers.{i}.lconv1d.depthwise_conv1d.weight",
                tensor.unsqueeze(1),
            )
            return

        if sub in _AUDIO_BLK_SUFFIX:
            yield (
                f"model.audio_tower.layers.{i}.{_AUDIO_BLK_SUFFIX[sub]}",
                tensor,
            )
            return


# ── top-level pass functions ───────────────────────────────────────────────────

def _finalize(t: torch.Tensor) -> torch.Tensor:
    """Cast to bfloat16 on CPU."""
    return t.contiguous().to(torch.bfloat16).cpu()


def text_pass(reader, device: torch.device):
    """Convert the text GGUF file → dict[str, torch.Tensor] (bf16, CPU)."""
    out: dict[str, torch.Tensor] = {}
    skipped: list[str] = []
    for t in reader.tensors:
        raw = load_gguf_tensor(t).to(device)
        any_yielded = False
        for hf_name, hf_tensor in convert_text_tensor(t.name, raw):
            out[hf_name] = _finalize(hf_tensor)
            any_yielded = True
        del raw
        if not any_yielded:
            skipped.append(t.name)
    if device.type == "cuda":
        torch.cuda.empty_cache()
    return out, skipped


def mmproj_pass(reader, device: torch.device):
    """Convert the mmproj GGUF file → dict[str, torch.Tensor] (bf16, CPU)."""
    out: dict[str, torch.Tensor] = {}
    skipped: list[str] = []
    for t in reader.tensors:
        raw = load_gguf_tensor(t).to(device)
        any_yielded = False
        for hf_name, hf_tensor in convert_vision_tensor(t.name, raw):
            out[hf_name] = _finalize(hf_tensor)
            any_yielded = True
        if not any_yielded:
            for hf_name, hf_tensor in convert_audio_tensor(t.name, raw):
                out[hf_name] = _finalize(hf_tensor)
                any_yielded = True
        del raw
        if not any_yielded:
            skipped.append(t.name)
    if device.type == "cuda":
        torch.cuda.empty_cache()
    return out, skipped


# ── buffer merge from reference safetensors ────────────────────────────────────

def merge_reference_buffers(
    gguf_tensors: dict[str, torch.Tensor],
    src_dir: Path,
) -> tuple[int, int]:
    """
    Fill in HF buffers that aren't in the GGUF by copying them from
    the reference repo's model.safetensors.
    """
    ref = src_dir / "model.safetensors"
    if not ref.exists():
        print(f"    [warn] Reference model.safetensors not found at {ref}")
        return (0, 0)

    copied = 0
    conflicts = 0
    with safe_open(str(ref), framework="pt") as s:
        for k in s.keys():
            if k in gguf_tensors:
                conflicts += 1
                continue
            gguf_tensors[k] = s.get_tensor(k)
            copied += 1
    return copied, conflicts


# ── sharded save ───────────────────────────────────────────────────────────────

def shard_and_save(tensors: dict[str, torch.Tensor], out_dir: Path, max_shard_gb: float):
    """Save tensors as sharded safetensors + index json."""
    out_dir.mkdir(parents=True, exist_ok=True)
    max_bytes = int(max_shard_gb * 1024**3)

    shards: list[dict[str, torch.Tensor]] = []
    cur_shard: dict[str, torch.Tensor] = {}
    cur_bytes = 0

    for k in sorted(tensors.keys()):
        v = tensors[k]
        nb = v.numel() * v.element_size()
        if cur_bytes + nb > max_bytes and cur_shard:
            shards.append(cur_shard)
            cur_shard = {}
            cur_bytes = 0
        cur_shard[k] = v
        cur_bytes += nb
    if cur_shard:
        shards.append(cur_shard)

    n = len(shards)
    weight_map: dict[str, str] = {}
    total_size = 0
    for idx, shard in enumerate(shards, 1):
        fname = f"model-{idx:05d}-of-{n:05d}.safetensors"
        shard_bytes = sum(v.numel() * v.element_size() for v in shard.values())
        total_size += shard_bytes
        print(f"  Saving shard {idx}/{n}: {fname}  ({shard_bytes/1e9:.2f} GB)")
        save_file(shard, str(out_dir / fname))
        for k in shard:
            weight_map[k] = fname

    index = {
        "metadata": {"total_size": total_size},
        "weight_map": weight_map,
    }
    (out_dir / "model.safetensors.index.json").write_text(json.dumps(index, indent=2))
    print(f"  Wrote index with {len(weight_map)} tensors across {n} shard(s).")


