def convert_vision_tensor(name: str, tensor: torch.Tensor):
    """Yield (hf_name, hf_tensor) for a vision-side mmproj GGUF tensor."""
    if name == "v.patch_embd.weight":
        n_embd, c, ph, pw = tensor.shape
        unreshaped = tensor.permute(0, 2, 3, 1).contiguous().reshape(n_embd, c * ph * pw)
        yield "model.vision_tower.patch_embedder.input_proj.weight", unreshaped
        return

    if name == "v.position_embd.weight":
        yield "model.vision_tower.patch_embedder.position_embedding_table", tensor
        return

    if name == "mm.input_projection.weight":
        yield "model.embed_vision.embedding_projection.weight", tensor
        return

    if name.startswith("v.blk."):
        parts = name.split(".", 3)
        try:
            i = int(parts[2])
        except ValueError:
            return
        sub = parts[3]
        if sub in _VISION_BLK_SUFFIX:
            yield (
                f"model.vision_tower.encoder.layers.{i}.{_VISION_BLK_SUFFIX[sub]}",
                tensor,
            )
        return


