#!/usr/bin/env python3
"""
gguf_to_hf_gemma4_e2b.py
Convert Gemma-4-E2B GGUF (language + mmproj) → HuggingFace safetensors
(Gemma4ForConditionalGeneration).

Architecture reference (from /mnt/DATA9/LLM_model/d/gemma-4-E2B/config.json):
  - 35 decoder layers, hidden_size=1536, head_dim=256 (sliding) / global_head_dim=512 (full)
  - Layer type pattern: 4x sliding + 1x full, repeating → full attention at layers
    4, 9, 14, 19, 24, 29, 34.
  - GQA: 8 query heads, 1 KV head per layer.
  - num_kv_shared_layers=20, use_double_wide_mlp=True → layers 15..34 have
    intermediate_size=12288 instead of 6144.
  - Per-layer-input pathway (hidden_size_per_layer_input=256) with:
      per_layer_input_gate, per_layer_projection, post_per_layer_input_norm,
      embed_tokens_per_layer, per_layer_model_projection, per_layer_projection_norm.
  - tie_word_embeddings=True → no lm_head.weight in state dict.
  - Vision tower: 16 conformer-style ViT blocks, hidden_size=768.
  - Audio tower: 12 conformer blocks, hidden_size=1024, with lconv1d module.

Reverse transforms applied (inverse of llama.cpp's convert_hf_to_gguf.py):

  Text (class Gemma4Model, inherits Gemma3Model):
    - Gemma4Model.norm_shift() overrides Gemma3 to return 0.0 for ALL names →
      NO (hf + 1) offset on any RMSNorm (unlike Gemma3/Qwen3.5).
    - blk.{i}.layer_output_scale.weight → model.language_model.layers.{i}.layer_scalar
      (buffer, not param — no ".weight" suffix on the HF side).
      Reference: Gemma4DecoderLayer.__init__ in modeling_gemma4.py registers
      `self.register_buffer("layer_scalar", torch.ones(1))`.
    - rope_freqs.weight is SKIPPED — it's generated by Gemma4Model.generate_extra_tensors
      during export and recomputed at model init on the HF side.
    - Q8_0 tensors are dequantized to float32 via gguf.quants.dequantize(); final
      save cast is bfloat16.

  Vision (class Gemma4VisionAudioModel.modify_tensors, is_audio_tensor=False path):
    - Renames "model.vision_tower.encoder." → "vision_model.model." and then strips
      ".linear.weight" → ".weight".  We do the inverse on read: add ".linear"
      to the q/k/v/o_proj and mlp gate/up/down projection weights.
    - patch_embedder.input_proj.weight: HF is (n_embd=768, 3*ph*pw=768) → during
      export, reshape to (n_embd, ph, pw, 3) then permute(0,3,1,2) → GGUF stores
      (768, 3, 16, 16).  Inverse: permute(0,2,3,1).reshape(n_embd, 3*ph*pw).
    - position_embedding_table is stored as-is (shape (2, 10240, 768)).
    - V_ENC_INPUT_NORM      = ln1           → input_layernorm
    - V_ENC_POST_ATTN_NORM  = ln2           → pre_feedforward_layernorm (NOT post-attn!)
    - V_ENC_ATTN_POST_NORM  = attn_post_norm → post_attention_layernorm  (gemma4 extra)
    - V_ENC_FFN_POST_NORM   = ffn_post_norm → post_feedforward_layernorm
    - V_ENC_EMBD_PATCH      = v.patch_embd  → patch_embedder.input_proj
    - V_ENC_EMBD_POS        = v.position_embd → patch_embedder.position_embedding_table
    - V_MM_INP_PROJ         = mm.input_projection → model.embed_vision.embedding_projection

  Audio (class Gemma4VisionAudioModel.modify_tensors, is_audio_tensor=True path):
    - Renames "model.audio_tower." → "conformer." then strips ".linear." → "."
      Inverse on read: put back ".linear." into the q_proj / k_proj / v_proj / post /
      lconv1d.linear_start / lconv1d.linear_end / feed_forward{1,2}.ffw_layer_{1,2} /
      subsample_conv_projection.layer{0,1}.conv paths.
    - Exception: relative_k_proj and per_dim_scale do NOT carry .linear (they are
      not wrapped in ClippedLinear on the HF side).
    - per_dim_scale is an nn.Parameter on the HF side (no ".weight" suffix).  The
      exporter appends ".weight" AND applies softplus(x) before writing.  Inverse
      on read: strip ".weight" + apply inverse softplus  y → log(exp(y) - 1).
    - lconv1d.depthwise_conv1d: HF weight is (C, 1, 5); the exporter reshapes to
      (C, 5) before writing (squeezes the middle dim).  Inverse on read: unsqueeze(1).
    - GGUF tensor name map:
        a.conv1d.{0,1}.weight   → subsample_conv_projection.layer{0,1}.conv.weight
        a.conv1d.{0,1}.norm.weight → subsample_conv_projection.layer{0,1}.norm.weight
        a.input_projection.weight  → subsample_conv_projection.input_proj_linear.weight
        a.blk.{i}.attn_q/k/v.weight → self_attn.{q,k,v}_proj.linear.weight
        a.blk.{i}.attn_out.weight    → self_attn.post.linear.weight   (NOT o_proj!)
        a.blk.{i}.attn_k_rel.weight  → self_attn.relative_k_proj.weight
        a.blk.{i}.attn_pre_norm      → norm_pre_attn
        a.blk.{i}.attn_post_norm     → norm_post_attn
        a.blk.{i}.ln2                → norm_out           (gemma4 reuses lfm2 slot)
        a.blk.{i}.ffn_norm           → feed_forward1.pre_layer_norm
        a.blk.{i}.ffn_post_norm      → feed_forward1.post_layer_norm
        a.blk.{i}.ffn_norm_1         → feed_forward2.pre_layer_norm
        a.blk.{i}.ffn_post_norm_1    → feed_forward2.post_layer_norm
        a.blk.{i}.ffn_up             → feed_forward1.ffw_layer_1.linear.weight
        a.blk.{i}.ffn_down           → feed_forward1.ffw_layer_2.linear.weight
        a.blk.{i}.ffn_up_1           → feed_forward2.ffw_layer_1.linear.weight
        a.blk.{i}.ffn_down_1         → feed_forward2.ffw_layer_2.linear.weight
        a.blk.{i}.conv_dw            → lconv1d.depthwise_conv1d
        a.blk.{i}.conv_norm          → lconv1d.pre_layer_norm
        a.blk.{i}.conv_pw1           → lconv1d.linear_start.linear.weight
        a.blk.{i}.conv_pw2           → lconv1d.linear_end.linear.weight
        a.blk.{i}.norm_conv          → lconv1d.conv_norm
        a.blk.{i}.per_dim_scale      → self_attn.per_dim_scale (inverse softplus)
        a.pre_encode.out.{weight,bias} → audio_tower.output_proj.{weight,bias}
        mm.a.input_projection.weight   → model.embed_audio.embedding_projection.weight

Buffers not present in GGUF (we copy these from the reference HF repo
  /mnt/DATA9/LLM_model/d/gemma-4-E2B/model.safetensors):
    - model.audio_tower.layers.*.{feed_forward{1,2}.ffw_layer_{1,2},
        self_attn.{q_proj,k_proj,v_proj,post},
        lconv1d.{linear_start,linear_end}}.{input,output}_{min,max}
    - model.audio_tower.layers.*.self_attn.softcap
    - model.vision_tower.encoder.layers.*.layer_scalar  (if present — the
      reference repo omits these since they default to ones()).

Non-persistent buffers (not in GGUF or reference safetensors; HF model
  recomputes them at __init__):
    - model.audio_tower.rel_pos_enc.inv_timescales  (sinusoidal)
    - model.*.embed_tokens.embed_scale              (sqrt(hidden_size))
    - model.embed_vision.embedding_projection.embed_scale
    - model.embed_audio.embedding_projection.embed_scale

Usage:
  python gguf_to_hf_gemma4_e2b.py --gguf PATH [--mmproj PATH] --src DIR
                                  --out DIR [--shard-gb SIZE] [--device DEV]
                                  [--skip-mmproj]
"""

