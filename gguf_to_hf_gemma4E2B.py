def convert_text_tensor(name: str, tensor: torch.Tensor):
    """Yield (hf_name, hf_tensor) pairs for one GGUF text tensor."""
    if name == "rope_freqs.weight":
        return

    if name in _TEXT_GLOBAL:
        yield f"model.language_model.{_TEXT_GLOBAL[name]}", tensor
        return

    if not name.startswith("blk."):
        return

    parts = name.split(".", 2)
    try:
        i = int(parts[1])
    except ValueError:
        return
    sub = parts[2]

    if sub not in _TEXT_BLK_SUFFIX:
        return
    hf_suffix = _TEXT_BLK_SUFFIX[sub]
    yield f"model.language_model.layers.{i}.{hf_suffix}", tensor


