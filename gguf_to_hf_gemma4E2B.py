# ── GGUF tensor loader ─────────────────────────────────────────────────────────

def _bf16_uint8_to_torch(raw: np.ndarray, t_shape) -> torch.Tensor:
    """
    GGUFReader returns BF16 tensors as uint8 with the last dimension's byte-count
    doubled.  Convert raw bytes → torch.bfloat16 with the logical shape.
    """
    assert raw.dtype == np.uint8
    buf = np.frombuffer(raw.tobytes(), dtype=np.uint16).copy()
    out = torch.from_numpy(buf).view(torch.bfloat16)
    logical_shape = raw.shape[:-1] + (raw.shape[-1] // 2,)
    return out.reshape(logical_shape)


def load_gguf_tensor(t) -> torch.Tensor:
    """
    Read a GGUFReader tensor → torch tensor in HF-native shape and the best
    available dtype (float32 for Q8_0, float16 for F16, float32 for F32,
    bfloat16 for BF16).

    For Q8_0 we use gguf.quants.dequantize() which unpacks the block-scale
    int8 layout and returns float32 with the logical (out, in) shape.
    """
    import gguf
    raw = np.array(t.data, copy=False)
    tt = t.tensor_type

    if tt == gguf.GGMLQuantizationType.F32:
        return torch.from_numpy(raw.copy()).to(torch.float32)
    if tt == gguf.GGMLQuantizationType.F16:
        return torch.from_numpy(raw.copy()).to(torch.float16)
    if tt == gguf.GGMLQuantizationType.BF16:
        return _bf16_uint8_to_torch(raw, tuple(int(x) for x in t.shape))
    deq = gguf.quants.dequantize(raw, tt)
    return torch.from_numpy(deq.copy()).to(torch.float32)


